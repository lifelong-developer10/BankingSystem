package mypack;


public class Bank {
private String name;
private int Accno;
private int Balance;
private String pin;
public Bank() {
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAccno() {
	return Accno;
}
public void setAccno(int accno) {
	Accno = accno;
}
public int getBalance() {
	return Balance;
}
public void setBalance(int balanace) {
	Balance = balanace;
}
public String getPin() {
	return pin;
}
public void setPin(String pin) {
	this.pin = pin;
}

}
